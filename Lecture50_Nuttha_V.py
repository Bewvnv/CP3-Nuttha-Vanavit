def addNumber(x,y):
    print(x,"+",y,"=",x+y)
def subtractNumber(x,y):
    print(x,"-",y,"=",x-y)
def multiplyNumber(x,y):
    print(x,"*",y,"=",x*y)
def divideNumber(x,y):
    print(x,"/",y,"=",x/y)

x = float(input("first number"))
y = float(input("second number"))
addNumber(x,y)
subtractNumber(x,y)
multiplyNumber(x,y)
divideNumber(x,y)
